// Single inheritance: A class inherits features from a single superclass. 
// Multilevel inheritance: A class inherits from a derived class, creating a "grandparent-parent-child" relationship. 
// Hierarchical inheritance: Multiple classes inherit from a single superclass. 
// Multiple inheritance: A class inherits attributes and behaviors from more than one superclass. 
// Hybrid inheritance: A combination of hierarchical inheritance and interface implementation, or multiple inheritance and hierarchical inheritance. 
